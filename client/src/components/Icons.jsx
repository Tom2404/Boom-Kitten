export function RankBadge({ rank = "Bronze IV", className = "w-5 h-5", showText = false }) {
  const [tier, division] = rank.split(' ');

  let badgeSvg = <BronzeBadge className={className} />;
  let tierBg = "bg-amber-100/80 text-amber-900 border-amber-400";

  switch (tier) {
    case 'Bronze':
      badgeSvg = <BronzeBadge className={className} />;
      tierBg = "bg-[#fef3c7] text-[#78350f] border-[#f59e0b]";
      break;
    case 'Silver':
      badgeSvg = <SilverBadge className={className} />;
      tierBg = "bg-[#f1f5f9] text-[#334155] border-[#94a3b8]";
      break;
    case 'Gold':
      badgeSvg = <GoldBadge className={className} />;
      tierBg = "bg-[#fef9c3] text-[#854d0e] border-[#eab308]";
      break;
    case 'Platinum':
      badgeSvg = <PlatinumBadge className={className} />;
      tierBg = "bg-[#f3e8ff] text-[#581c87] border-[#c084fc]";
      break;
    case 'Diamond':
      badgeSvg = <DiamondBadge className={className} />;
      tierBg = "bg-[#ecfeff] text-[#164e63] border-[#22d3ee]";
      break;
    case 'Legend':
      badgeSvg = <LegendBadge className={className} />;
      tierBg = "bg-[#fee2e2] text-[#7f1d1d] border-[#ef4444] font-black";
      break;
    default:
      break;
  }

  return (
    <div className={`inline-flex items-center gap-1.5 px-2 py-0.5 rounded-xl border-2 shadow-[1.5px_1.5px_0px_0px_rgba(26,28,28,1)] select-none ${tierBg}`}>
      {badgeSvg}
      {showText && (
        <span className="font-headline font-black text-[9px] uppercase tracking-wider">
          {tier} {division || ''}
        </span>
      )}
    </div>
  );
}

